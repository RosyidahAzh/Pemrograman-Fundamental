package RosyidAzh;

public class Trapesium {
    public static void main(String[] args) {

        int a = 9;
        int b = 7;
        int t = 8;
        int m = 3;

        double Luas = 0.5 * (a + b) * t;
        double Keliling = a + b + 2 * m;

        System.out.println("Luas Trapesium :" + Luas);
        System.out.println("Keliling Trapesium :" + Keliling);

    }
}